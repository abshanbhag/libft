char	*ft_strrchr(const char *s, int c)
{
	int a;

	a = 0;
	while (s[a] != '\0')
		a++;
	while (a >= 0)
	{
		if (s[a] == (char)c)
			return ((char*)&s[a]);
		a--;
	}
	return (0);
}
