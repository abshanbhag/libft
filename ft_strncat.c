#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t b;

	i = 0;
	b = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[b] != '\0' && b < n)
	{
		s1[i + b] = s2[b];
		b++;
	}
	s1[i + b] = '\0';
	return (s1);
}
